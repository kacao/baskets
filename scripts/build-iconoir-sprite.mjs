// Generates an external SVG sprite + a names list from the `iconoir` package.
//   static/iconoir.svg   — one <symbol id="<name>"> per regular icon (served at
//                          /iconoir.svg; <use href="/iconoir.svg#name"> renders one)
//   src/lib/iconoirNames.ts — the searchable list of icon names
// Run via `npm run icons:build` (also wired as a prebuild step).
import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const srcDir = join(root, 'node_modules', 'iconoir', 'icons', 'regular');
const spriteOut = join(root, 'static', 'iconoir.svg');
const namesOut = join(root, 'src', 'lib', 'iconoirNames.ts');

const files = readdirSync(srcDir)
	.filter((f) => f.endsWith('.svg'))
	.sort();

const names = [];
const symbols = [];

for (const file of files) {
	const name = file.slice(0, -4); // drop .svg
	const raw = readFileSync(join(srcDir, file), 'utf8');
	const viewBox = (raw.match(/viewBox="([^"]+)"/) || [, '0 0 24 24'])[1];
	// inner markup (paths) between the <svg> tags — paths already use currentColor
	const inner = raw
		.replace(/^[\s\S]*?<svg[^>]*>/, '')
		.replace(/<\/svg>\s*$/, '')
		.trim();
	names.push(name);
	symbols.push(
		`<symbol id="${name}" viewBox="${viewBox}" fill="none" stroke-width="1.5">${inner}</symbol>`
	);
}

const sprite = `<svg xmlns="http://www.w3.org/2000/svg" style="display:none">${symbols.join('')}</svg>\n`;

mkdirSync(dirname(spriteOut), { recursive: true });
writeFileSync(spriteOut, sprite);

const namesFile = `// AUTO-GENERATED by scripts/build-iconoir-sprite.mjs — do not edit by hand.
// ${names.length} iconoir icon names (https://iconoir.com). Run \`npm run icons:build\`.
export const ICONOIR_NAMES: string[] = ${JSON.stringify(names)};
`;
writeFileSync(namesOut, namesFile);

console.log(`iconoir: wrote ${names.length} symbols → static/iconoir.svg + src/lib/iconoirNames.ts`);
